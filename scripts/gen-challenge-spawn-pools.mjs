#!/usr/bin/env node
// Generate the Challenge CPU spawn-pool roster table recovered from boot.dol.
//
// Source table:
//   research/decomp/data/spawn-pools-80380804.json
//
// The raw DOL table is indexed by "group code" and stores 16-bit spawn ids as
// family<<8 | variant. Challenge setup uses group codes 48..69; convert those
// entries to browser/runtime borg ids (`pl####`) for @gf/missions.

import { createHash } from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRel = "research/decomp/data/spawn-pools-80380804.json";
const outRel = "packages/missions/src/challenge-spawn-pools.generated.ts";
const sourcePath = path.join(repoRoot, sourceRel);
const outPath = path.join(repoRoot, outRel);
const GROUP_START = 48;
const GROUP_END = 69;

function sha1(text) {
  return createHash("sha1").update(text).digest("hex");
}

function borgId([family, variant]) {
  return `pl${family.toString(16).padStart(2, "0")}${variant.toString(16).padStart(2, "0")}`;
}

function quoteList(values) {
  return `[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

const sourceText = fs.readFileSync(sourcePath, "utf8");
const data = JSON.parse(sourceText);
const byGroup = new Map((data.groups ?? []).map((group) => [group.group, group]));
const rows = [];
let totalBorgIds = 0;

for (let group = GROUP_START; group <= GROUP_END; group += 1) {
  const record = byGroup.get(group);
  if (!record) throw new Error(`missing spawn-pool group ${group}`);
  const ids = (record.ids_family_variant ?? []).map(borgId);
  totalBorgIds += ids.length;
  rows.push({ group, ids });
}

const source = `// Generated by scripts/gen-challenge-spawn-pools.mjs.
// Source: ${sourceRel} (${data.address})
// Do not hand-edit; rerun the generator after updating the decomp data.

export const CHALLENGE_GROUP_ROSTERS = {
${rows.map((row) => `  ${row.group}: ${quoteList(row.ids)},`).join("\n")}
} as const;

export const CHALLENGE_GROUP_ROSTER_SOURCE = {
  address: ${JSON.stringify(data.address)},
  sourcePath: ${JSON.stringify(sourceRel)},
  sourceSha1: ${JSON.stringify(sha1(sourceText))},
  groupStart: ${GROUP_START},
  groupEnd: ${GROUP_END},
  groupCount: ${rows.length},
  totalBorgIds: ${totalBorgIds},
  description: "Challenge group rosters converted from family<<8|variant spawn ids in the recovered 0x80380804 DOL table.",
} as const;
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, source);
console.log(`wrote ${outRel}; groups ${rows.length}; borg ids ${totalBorgIds}`);
