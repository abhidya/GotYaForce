#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = path.join(repoRoot, "apps", "game", "public", "stages", "manifest.json");
const outPath = path.join(repoRoot, "apps", "game", "src", "stages", "catalog.generated.ts");

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
const stages = Array.isArray(manifest.stages) ? manifest.stages : [];
if (stages.length === 0) throw new Error(`stage manifest has no stages: ${rel(manifestPath)}`);

const catalog = stages
  .map((stage) => ({
    id: requiredStageId(stage.id),
    status: String(stage.status ?? "unknown"),
    modelCount: finiteInt(stage.modelCount),
    renderStatus: String(stage.renderStatus ?? "unknown"),
    visualComplete: stage.visualComplete === true,
    collisionCount: finiteInt(stage.collisionCount),
    setArcCount: finiteInt(stage.setArcCount),
  }))
  .sort((a, b) => a.id.localeCompare(b.id));

if (!catalog.some((stage) => stage.id === "st00")) {
  throw new Error("stage catalog must include st00 default arena");
}

const source = `// Generated by scripts/build-stage-catalog-module.mjs from apps/game/public/stages/manifest.json.
// Do not hand-edit. Rerun \`npm run manifest:stage-catalog\` after stage exports change.

export type StageCatalogEntry = {
  id: string;
  status: string;
  modelCount: number;
  renderStatus: string;
  visualComplete: boolean;
  collisionCount: number;
  setArcCount: number;
};

export const EXPORTED_STAGE_CATALOG = ${JSON.stringify(catalog, null, 2)} as const satisfies readonly StageCatalogEntry[];

export type ExportedStageId = (typeof EXPORTED_STAGE_CATALOG)[number]["id"];

export const EXPORTED_STAGE_IDS = EXPORTED_STAGE_CATALOG.map((stage) => stage.id) as readonly ExportedStageId[];

export const DEFAULT_ARENA_STAGE: ExportedStageId = "st00";

const EXPORTED_STAGE_ID_SET = new Set<string>(EXPORTED_STAGE_IDS);

export function isExportedStageId(value: string): value is ExportedStageId {
  return EXPORTED_STAGE_ID_SET.has(value);
}

export function stageCatalogEntry(id: string): StageCatalogEntry | null {
  return EXPORTED_STAGE_CATALOG.find((stage) => stage.id === id) ?? null;
}
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, source);
console.log(`wrote ${rel(outPath)} (${catalog.length} stages)`);

function requiredStageId(value) {
  const id = String(value ?? "").trim().toLowerCase();
  if (!/^st[0-9a-f]{2}$/.test(id)) throw new Error(`invalid stage id in manifest: ${value}`);
  return id;
}

function finiteInt(value) {
  const n = Number(value);
  return Number.isFinite(n) ? Math.trunc(n) : 0;
}

function rel(file) {
  return path.relative(repoRoot, file).replaceAll("\\", "/");
}
