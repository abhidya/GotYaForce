#!/usr/bin/env node
// Generate the ROM-confirmed constants for the knockback direction port.
//
// Source:
//   research/decomp/data/knockback-direction-800300bc.json
//
// Runtime consumes these through packages/physics/src/knockback.ts.

import { createHash } from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRel = "research/decomp/data/knockback-direction-800300bc.json";
const outRel = "packages/physics/src/knockback.generated.ts";

function sha1(text) {
  return createHash("sha1").update(text).digest("hex");
}

const sourcePath = path.join(repoRoot, sourceRel);
const sourceText = fs.readFileSync(sourcePath, "utf8");
const data = JSON.parse(sourceText);
const constants = data.confirmed_rom_constants ?? {};

const zero = constants.FLOAT_80436f68?.value;
const fallbackZ = constants.FLOAT_80436f74?.value;
const threshold = constants.FLOAT_80436fc0?.value;
const bamPerRadian = constants.FLOAT_8043707c?.value;
const modeValues = (data.modes ?? []).map((mode) => mode.value);

if (data.address !== "0x800300bc") throw new Error(`unexpected knockback function address: ${data.address}`);
if (zero !== 0 || fallbackZ !== -1) throw new Error("unexpected fallback vector constants");
if (typeof threshold !== "number" || threshold <= 0) throw new Error("missing degenerate threshold");
if (typeof bamPerRadian !== "number" || bamPerRadian <= 0) throw new Error("missing BAM16 conversion constant");
if (modeValues.join(",") !== "0,1,2,3,4") throw new Error(`unexpected knockback mode values: ${modeValues.join(",")}`);

const source = `// Generated by scripts/gen-knockback-direction-data.mjs.
// Source: ${sourceRel} (${data.address})
// Do not hand-edit; rerun the generator after updating decomp data.

export const KNOCKBACK_DIRECTION_SOURCE = {
  address: ${JSON.stringify(data.address)},
  sourcePath: ${JSON.stringify(sourceRel)},
  sourceSha1: ${JSON.stringify(sha1(sourceText))},
  modeValues: ${JSON.stringify(modeValues)},
  modeCount: ${modeValues.length},
  description: "ROM-confirmed constants and mode values for zz_00300bc_ knockback/launch direction.",
} as const;

/** FLOAT_8043707c: radians to BAM16 conversion. */
export const BAM16_PER_RADIAN = ${JSON.stringify(bamPerRadian)};

/** FLOAT_80436fc0: degenerate-vector magnitude-squared threshold. */
export const DEGENERATE_MAG_SQ_THRESHOLD = ${JSON.stringify(threshold)};

/** FLOAT_80436f68/FLOAT_80436f74: fallback vector when the source vector is degenerate. */
export const FALLBACK_DIR_COMPONENTS = {
  x: ${JSON.stringify(zero)},
  y: ${JSON.stringify(zero)},
  z: ${JSON.stringify(fallbackZ)},
} as const;

export const BAM16_WRAP = 0x10000;
`;

const outPath = path.join(repoRoot, outRel);
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, source);
console.log(`wrote ${outRel}; modes ${modeValues.length}; BAM16/rad ${bamPerRadian}`);
