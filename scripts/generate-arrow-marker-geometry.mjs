#!/usr/bin/env node
import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const region = "GG4E";
const arcRel = `user-data/${region}/afs_data/root/arrow_mdl.arc`;
const objRel = `user-data/${region}/hsd-test-python/arrow_mdl.obj`;
const outRel = "apps/game/src/sim/data/arrowMdlGeometry.generated.ts";

function abs(rel) {
  return path.join(repoRoot, rel);
}

function sha1(buffer) {
  return createHash("sha1").update(buffer).digest("hex");
}

function parseObj(text) {
  const vertices = [];
  const triangles = [];
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith("#")) continue;
    const parts = line.split(/\s+/);
    if (parts[0] === "v") {
      if (parts.length < 4) throw new Error(`bad vertex line: ${line}`);
      vertices.push(parts.slice(1, 4).map((part) => Number(part)));
    } else if (parts[0] === "f") {
      const indices = parts.slice(1).map((part) => {
        const rawIndex = Number(part.split("/")[0]);
        if (!Number.isInteger(rawIndex) || rawIndex <= 0) throw new Error(`bad face index in line: ${line}`);
        return rawIndex - 1;
      });
      for (let i = 1; i + 1 < indices.length; i++) {
        triangles.push([indices[0], indices[i], indices[i + 1]]);
      }
    }
  }
  if (!vertices.length || !triangles.length) {
    throw new Error(`OBJ did not contain vertices and triangles: ${objRel}`);
  }
  return { vertices, triangles };
}

function boundsFor(vertices) {
  const min = [Infinity, Infinity, Infinity];
  const max = [-Infinity, -Infinity, -Infinity];
  for (const v of vertices) {
    for (let i = 0; i < 3; i++) {
      min[i] = Math.min(min[i], v[i]);
      max[i] = Math.max(max[i], v[i]);
    }
  }
  const center = min.map((value, i) => (value + max[i]) / 2);
  const size = min.map((value, i) => max[i] - value);
  return { min, max, center, size };
}

function fmtArray(values, perLine = 12) {
  const rows = [];
  for (let i = 0; i < values.length; i += perLine) {
    rows.push(`  ${values.slice(i, i + perLine).map((value) => Number(value.toFixed(6))).join(", ")}`);
  }
  return `[\n${rows.join(",\n")}\n]`;
}

const [arcBytes, objBytes] = await Promise.all([readFile(abs(arcRel)), readFile(abs(objRel))]);
const { vertices, triangles } = parseObj(objBytes.toString("utf8"));
const flatPositions = vertices.flat();
const flatIndices = triangles.flat();
const bounds = boundsFor(vertices);

if (vertices.length > 65535) {
  throw new Error(`arrow_mdl has ${vertices.length} vertices; update runtime index type`);
}

const source = `// Generated by scripts/generate-arrow-marker-geometry.mjs.
// Source geometry: ${objRel}
// Source archive: ${arcRel}
// Do not hand-edit; rerun the generator after updating the HSDRaw OBJ export.

export const ARROW_MDL_SOURCE = {
  region: ${JSON.stringify(region)},
  archivePath: ${JSON.stringify(arcRel)},
  archiveSha1: ${JSON.stringify(sha1(arcBytes))},
  objPath: ${JSON.stringify(objRel)},
  objSha1: ${JSON.stringify(sha1(objBytes))},
  vertexCount: ${vertices.length},
  triangleCount: ${triangles.length},
} as const;

export const ARROW_MDL_BOUNDS = {
  min: ${JSON.stringify(bounds.min)},
  max: ${JSON.stringify(bounds.max)},
  center: ${JSON.stringify(bounds.center)},
  size: ${JSON.stringify(bounds.size)},
} as const;

export const ARROW_MDL_POSITIONS = new Float32Array(${fmtArray(flatPositions)});

export const ARROW_MDL_INDICES = new Uint16Array(${fmtArray(flatIndices)});
`;

await mkdir(path.dirname(abs(outRel)), { recursive: true });
await writeFile(abs(outRel), source, "utf8");
console.log(`wrote ${outRel} (${vertices.length} verts, ${triangles.length} tris)`);
