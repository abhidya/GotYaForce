#!/usr/bin/env node
// Generate the TUNED burn-down checklist: research/tuned-burndown.md
//
// The repo's confidence convention (packages/combat/src/constants.ts header) labels every
// reverse-engineered value DERIVED (ROM-confirmed, evidence cited) or TUNED (honest guess).
// Those labels ARE the porting TODO list — this script turns them into a countable report
// so "how close is the port to 1:1" is a number, not a feeling. Re-run any time; derived
// output, never hand-edited.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SCAN_ROOTS = ["packages", "apps/game/src"];
const OUT = path.join(repoRoot, "research", "tuned-burndown.md");

const files = [];
(function walk(dir) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    if (e.name === "node_modules" || e.name === "dist" || e.name.startsWith(".")) continue;
    const p = path.join(dir, e.name);
    if (e.isDirectory()) walk(p);
    else if (/\.(ts|tsx|mts)$/.test(e.name) && !e.name.endsWith(".d.ts")) files.push(p);
  }
})(...SCAN_ROOTS.map((r) => path.join(repoRoot, r)).filter(fs.existsSync));

// A marker line mentions TUNED or DERIVED in a comment. Classify the strongest claim on
// the line: TUNED-with-DERIVED-mechanism counts as TUNED (the value still needs evidence).
const rows = [];
for (const file of files) {
  const rel = path.relative(repoRoot, file).replace(/\\/g, "/");
  const lines = fs.readFileSync(file, "utf8").split(/\r?\n/);
  lines.forEach((line, i) => {
    if (!/\b(TUNED|DERIVED)\b/.test(line)) return;
    const kind = /\bTUNED\b/.test(line) ? "TUNED" : "DERIVED";
    const text = line.trim().slice(0, 160);
    rows.push({ file: rel, line: i + 1, kind, text });
  });
}

const tuned = rows.filter((r) => r.kind === "TUNED");
const derived = rows.filter((r) => r.kind === "DERIVED");
const byFile = new Map();
for (const r of tuned) {
  if (!byFile.has(r.file)) byFile.set(r.file, []);
  byFile.get(r.file).push(r);
}

const md = [
  "# TUNED burn-down",
  "",
  `Generated by \`scripts/report-tuned-constants.mjs\`. Re-run after any constants change.`,
  "",
  `A TUNED marker is a value still awaiting ROM/trace evidence; a DERIVED marker cites its`,
  `evidence. The port is 1:1 when this list is empty (or every remaining entry is an`,
  `intentional, documented simplification).`,
  "",
  `## Score`,
  "",
  `- **TUNED markers remaining: ${tuned.length}**`,
  `- DERIVED markers: ${derived.length}`,
  `- Files with TUNED debt: ${byFile.size}`,
  "",
  "## TUNED markers by file",
  "",
  ...[...byFile.entries()]
    .sort((a, b) => b[1].length - a[1].length)
    .flatMap(([file, list]) => [
      `### ${file} (${list.length})`,
      "",
      ...list.map((r) => `- [ ] \`${file}:${r.line}\` — ${r.text.replace(/\|/g, "\\|")}`),
      "",
    ]),
].join("\n");

fs.writeFileSync(OUT, md + "\n");
console.log(`TUNED remaining: ${tuned.length} (in ${byFile.size} files); DERIVED: ${derived.length}`);
console.log(`wrote ${path.relative(repoRoot, OUT)}`);
