#!/usr/bin/env node
// Generate the real type/category damage tables recovered from GG4E boot.dol.
//
// Sources:
//   - research/decomp/data/type-category-remap-802f2e28.json
//   - research/decomp/data/type-multiplier-matrix-802c5d60.json
//
// Runtime consumes these through packages/combat/src/typeDamage.ts.

import { createHash } from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const remapRel = "research/decomp/data/type-category-remap-802f2e28.json";
const matrixRel = "research/decomp/data/type-multiplier-matrix-802c5d60.json";
const borgsRel = "packages/assets/data/borgs.json";
const outRel = "packages/combat/src/typeDamage.generated.ts";

function readJsonWithText(relPath) {
  const text = fs.readFileSync(path.join(repoRoot, relPath), "utf8");
  return { text, json: JSON.parse(text) };
}

function sha1(text) {
  return createHash("sha1").update(text).digest("hex");
}

function tableLiteral(value) {
  return JSON.stringify(value, null, 2).replace(/"([^"]+)":/g, "$1:");
}

function parseBorgId(id) {
  const match = /^pl([0-9a-f]{2})([0-9a-f]{2})$/i.exec(id);
  if (!match) return null;
  return [Number.parseInt(match[1], 16), Number.parseInt(match[2], 16)];
}

const remapSource = readJsonWithText(remapRel);
const matrixSource = readJsonWithText(matrixRel);
const borgsSource = readJsonWithText(borgsRel);

const remap = remapSource.json.rows;
const matrix = matrixSource.json.matrix;
if (!Array.isArray(remap) || remap.length !== 16 || remap.some((row) => !Array.isArray(row))) {
  throw new Error("type-category remap must be a 16-row jagged numeric table");
}
if (
  !Array.isArray(matrix) ||
  matrix.length !== 20 ||
  matrix.some((row) => !Array.isArray(row) || row.length !== 20 || row.some((value) => typeof value !== "number"))
) {
  throw new Error("type multiplier matrix must be a 20x20 numeric table");
}

const borgs = borgsSource.json.borgs ?? [];
const missing = [];
for (const borg of borgs) {
  const parsed = parseBorgId(String(borg.id));
  if (!parsed) {
    missing.push({ id: borg.id, reason: "id does not match pl####" });
    continue;
  }
  const [family, variant] = parsed;
  const category = remap[family]?.[variant];
  if (category === undefined) missing.push({ id: borg.id, family, variant });
}

if (missing.length > 0) {
  console.error("type damage remap does not cover every borgs.json id");
  console.error(JSON.stringify(missing.slice(0, 20), null, 2));
  process.exit(1);
}

const source = `// Generated by scripts/gen-type-damage-tables.mjs.
// Sources:
//   ${remapRel} (${remapSource.json.address})
//   ${matrixRel} (${matrixSource.json.address})
// Do not hand-edit; rerun the generator after updating decomp data.

export const TYPE_CATEGORY_REMAP = ${tableLiteral(remap)} as const;

export const TYPE_MULTIPLIER_MATRIX = ${tableLiteral(matrix)} as const;

export const TYPE_DAMAGE_TABLE_SOURCE = {
  remapAddress: ${JSON.stringify(remapSource.json.address)},
  matrixAddress: ${JSON.stringify(matrixSource.json.address)},
  remapSourcePath: ${JSON.stringify(remapRel)},
  matrixSourcePath: ${JSON.stringify(matrixRel)},
  remapSourceSha1: ${JSON.stringify(sha1(remapSource.text))},
  matrixSourceSha1: ${JSON.stringify(sha1(matrixSource.text))},
  remapRows: ${remap.length},
  matrixRows: ${matrix.length},
  matrixColumns: ${matrix[0]?.length ?? 0},
  mappedBorgIds: ${borgs.length},
  description: "Real GG4E type/category damage multiplier tables: object+1000 family/variant remap, then matrix[defenderCategory][attackerCategory].",
} as const;
`;

const outPath = path.join(repoRoot, outRel);
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, source);
console.log(`wrote ${outRel}; remap rows ${remap.length}; matrix ${matrix.length}x${matrix[0]?.length ?? 0}; borg ids ${borgs.length}`);
